int	checkduplicate(int *s_l)
{
	int	i;

	i = 0;
	if (s_l[i] == 0 && s_l[i + 1] == 0)
		return (0);
	while (s_l[i + 1])
	{
		if (s_l[i] == s_l[i + 1])
			return (0);
		i++;
	}
	return (1);
}