int	checkduplicate(int *s_l, int size)
{
	int	i;
	int	x;

	i = 0;
	while (i < size)
	{
		x = i + 1;
		while (x < size)
		{
			if (x != i && s_l[i] == s_l[x])
				return (0);
			x++;
		}
		i++;
	}
	return (1);
}
